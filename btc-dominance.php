  <ul class="en">
    <li><strong>Bear markets / uncertainty:</strong> Investors flock to the relatively safer Bitcoin, pushing dominance up</li>
    <li><strong>Late bull market:</strong> Profits taken from Bitcoin rotate into higher-beta altcoins, pushing dominance down</li>
    <li><strong>Fresh institutional inflows (e.g. ETFs):</strong> Institutional capital tends to enter Bitcoin first, initially pushing dominance up</li>
  </ul>
