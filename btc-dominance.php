    <li><strong>Final del mercado alcista:</strong> Las ganancias tomadas de Bitcoin rotan hacia altcoins de mayor beta, empujando la dominancia a la baja</li>
    <li><strong>Nuevos flujos institucionales (ej. ETFs):</strong> El capital institucional tiende a entrar primero en Bitcoin, presionando inicialmente la dominancia al alza</li>
  </ul>
