  <div class="box en">💡 <strong>Key takeaway:</strong> Rising dominance means risk-off (capital concentrating in Bitcoin); falling dominance means risk-on (speculation spreading into altcoins). A sharp dominance rollover is often used as an altseason entry signal.</div>
  <div class="box ja">💡 <strong>要点:</strong> ドミナンス上昇はリスク回避(資金がビットコインに集中)、下落はリスク選好(投機がアルトコインに拡散)を意味します。ドミナンスが急激に下降する時点が、アルトシーズン突入のシグナルとして使われます。</div>
  <div class="box es">💡 <strong>Resumen clave:</strong> El aumento de dominancia significa aversión al riesgo (capital concentrándose en Bitcoin); su caída significa apetito por riesgo (especulación dispersándose en altcoins). Una caída brusca de dominancia se usa a menudo como señal de entrada a la temporada alt.</div>
  <div class="box de">💡 <strong>Kernaussage:</strong> Steigende Dominanz bedeutet Risk-Off (Kapital konzentriert sich in Bitcoin); fallende Dominanz bedeutet Risk-On (Spekulation breitet sich in Altcoins aus). Ein scharfer Dominanz-Umschwung wird oft als Alt-Season-Einstiegssignal genutzt.</div>

