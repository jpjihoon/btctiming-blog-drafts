  <h2 class="es">Por Qué Sube y Baja la Dominancia</h2>
  <h2 class="de">Warum die Dominanz steigt und fällt</h2>
  <ul class="ko">
    <li><strong>약세장/불확실성 시기:</strong> 투자자들이 상대적으로 안전한 비트코인으로 몰리며 도미넌스 상승</li>
    <li><strong>강세장 후반부:</strong> 비트코인에서 이익을 실현한 자금이 고베타 알트코인으로 이동하며 도미넌스 하락</li>
    <li><strong>신규 자금 유입기(ETF 등):</strong> 기관 자금은 대부분 비트코인 먼저 유입되므로 초기엔 도미넌스 상승 압력</li>
  </ul>
  <ul class="en">
    <li><strong>Bear markets / uncertainty:</strong> Investors flock to the relatively safer Bitcoin, pushing dominance up</li>
    <li><strong>Late bull market:</strong> Profits taken from Bitcoin rotate into higher-beta altcoins, pushing dominance down</li>
    <li><strong>Fresh institutional inflows (e.g. ETFs):</strong> Institutional capital tends to enter Bitcoin first, initially pushing dominance up</li>
  </ul>
  <ul class="ja">
    <li><strong>弱気相場/不確実性の時期:</strong> 投資家が相対的に安全なビットコインに集まり、ドミナンスが上昇</li>
    <li><strong>強気相場の後半:</strong> ビットコインで利益確定した資金が高ベータのアルトコインへ移動し、ドミナンスが下落</li>
    <li><strong>新規資金流入期(ETFなど):</strong> 機関資金はまずビットコインに流入する傾向があるため、初期はドミナンス上昇圧力に</li>
  </ul>
  <ul class="es">
    <li><strong>Mercados bajistas/incertidumbre:</strong> Los inversores acuden al Bitcoin relativamente más seguro, empujando la dominancia al alza</li>
    <li><strong>Final del mercado alcista:</strong> Las ganancias tomadas de Bitcoin rotan hacia altcoins de mayor beta, empujando la dominancia a la baja</li>
    <li><strong>Nuevos flujos institucionales (ej. ETFs):</strong> El capital institucional tiende a entrar primero en Bitcoin, presionando inicialmente la dominancia al alza</li>
  </ul>
  <ul class="de">
    <li><strong>Bärenmärkte/Unsicherheit:</strong> Investoren strömen zum relativ sichereren Bitcoin, was die Dominanz nach oben treibt</li>
    <li><strong>Später Bullenmarkt:</strong> Aus Bitcoin genommene Gewinne rotieren in Altcoins mit höherem Beta, was die Dominanz nach unten drückt</li>
    <li><strong>Neue institutionelle Zuflüsse (z.B. ETFs):</strong> Institutionelles Kapital fließt tendenziell zuerst in Bitcoin, was die Dominanz zunächst nach oben treibt</li>
  </ul>

