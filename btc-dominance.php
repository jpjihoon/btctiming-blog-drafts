  <p class="ko"><strong>BTC 도미넌스</strong>는 전체 암호화폐 시가총액 중 비트코인이 차지하는 비율입니다. 이 숫자가 오르내리는 흐름만 봐도 지금 시장 자금이 "안전자산(비트코인)"으로 몰리는지, "위험자산(알트코인)"으로 확산되는지를 읽을 수 있습니다.</p>
  <p class="en"><strong>BTC Dominance</strong> is Bitcoin's share of total cryptocurrency market cap. Just watching this number rise or fall reveals whether capital is flowing toward the "safer asset" (Bitcoin) or spreading into "riskier assets" (altcoins).</p>
  <p class="ja"><strong>BTCドミナンス</strong>は、暗号資産市場全体の時価総額に占めるビットコインの割合です。この数字が上下する流れを見るだけで、今市場の資金が「安全資産(ビットコイン)」に集まっているのか、「リスク資産(アルトコイン)」に拡散しているのかを読み取れます。</p>

  <p class="es">La <strong>Dominancia de BTC</strong> es la proporción de Bitcoin en la capitalización total del mercado de criptomonedas. Solo observando si este número sube o baja se puede saber si el capital fluye hacia el "activo más seguro" (Bitcoin) o se dispersa hacia "activos más riesgosos" (altcoins).</p>
  <p class="de">Die <strong>BTC-Dominanz</strong> ist Bitcoins Anteil an der gesamten Kryptowährungs-Marktkapitalisierung. Allein durch Beobachtung, ob diese Zahl steigt oder fällt, lässt sich erkennen, ob Kapital in das "sicherere Asset" (Bitcoin) fließt oder sich in "riskantere Assets" (Altcoins) ausbreitet.</p>

