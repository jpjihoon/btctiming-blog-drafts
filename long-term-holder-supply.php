  <p class="de">Die Verfolgung, wie viel von Bitcoins Gesamtangebot bei <strong>"starken Händen"</strong> liegt — Haltern, die seit über 155 Tagen keine Münzen bewegt haben — zeigt die zugrunde liegende Marktüberzeugung. Dieses Verhältnis wird <strong>Long-Term-Holder-(LTH)-Angebot</strong> genannt.</p>
