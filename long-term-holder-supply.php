  <p class="en">Tracking how much of Bitcoin's total supply sits with <strong>"strong hands"</strong> — holders who haven't moved coins in over 155 days — reveals the market's underlying conviction. This ratio is called <strong>Long-Term Holder (LTH) Supply</strong>.</p>
  <p class="ja">ビットコインの総供給量のうち、どれだけのコインが<strong>「強い手」</strong>、つまり155日以上動かされていない長期保有者に保有されているかを追跡すると、市場の確信度を把握できます。この比率を<strong>LTH(長期保有者)供給比率</strong>と呼びます。</p>

