  <h2 class="ko">주의할 점</h2>
  <h2 class="en">Important Caveats</h2>
  <h2 class="ja">注意すべき点</h2>
  <h2 class="es">Advertencias Importantes</h2>
  <h2 class="de">Wichtige Einschränkungen</h2>
  <ul class="ko">
    <li><strong>후행 지표.</strong> 200주 평균이므로 가격 급변을 즉각 반영하지 못하고 서서히 따라갑니다.</li>
    <li><strong>사이클이 성숙할수록 괴리 축소 가능.</strong> 시가총액이 커질수록 진폭이 줄어들며 200WMA와의 거리도 좁아지는 추세입니다.</li>
    <li><strong>단독 매매 신호로는 부족.</strong> 정확한 저점 타이밍이 아니라 "장기적으로 싼 구간"을 알려주는 용도로 적합합니다.</li>
  </ul>
  <ul class="en">
    <li><strong>Lagging by nature.</strong> As a 200-week average, it moves slowly and doesn't immediately reflect sharp price swings.</li>
    <li><strong>Gap may narrow as the market matures.</strong> As market cap grows, volatility tends to shrink, narrowing the distance to the 200WMA over time.</li>
    <li><strong>Not a precise standalone signal.</strong> Better used to identify "cheap on a long-term basis" than to pinpoint an exact bottom.</li>
  </ul>
  <ul class="ja">
    <li><strong>後行指標である。</strong> 200週平均のため、価格の急変を即座には反映せず、緩やかに追従します。</li>
    <li><strong>市場が成熟するほど乖離が縮小する可能性。</strong> 時価総額が大きくなるほど振幅が小さくなり、200WMAとの距離も狭まる傾向にあります。</li>
    <li><strong>単独の売買シグナルとしては不十分。</strong> 正確な底値のタイミングではなく「長期的に見て割安な水準」を示す用途に適しています。</li>
  </ul>
  <ul class="es">
    <li><strong>Rezagado por naturaleza.</strong> Como promedio de 200 semanas, se mueve lentamente y no refleja inmediatamente oscilaciones bruscas de precio.</li>
    <li><strong>La brecha puede reducirse a medida que el mercado madura.</strong> A medida que crece la capitalización de mercado, la volatilidad tiende a reducirse, acortando la distancia al 200WMA con el tiempo.</li>
    <li><strong>No es una señal precisa independiente.</strong> Mejor usado para identificar "barato en base a largo plazo" que para precisar un suelo exacto.</li>
  </ul>
  <ul class="de">
    <li><strong>Von Natur aus nachlaufend.</strong> Als 200-Wochen-Durchschnitt bewegt er sich langsam und spiegelt scharfe Preisschwankungen nicht sofort wider.</li>
    <li><strong>Abstand kann sich mit Marktreife verringern.</strong> Mit wachsender Marktkapitalisierung tendiert die Volatilität zur Schrumpfung, was den Abstand zum 200WMA im Laufe der Zeit verringert.</li>
    <li><strong>Kein präzises eigenständiges Signal.</strong> Besser geeignet, um "langfristig günstig" zu identifizieren, als ein genaues Tief zu bestimmen.</li>
