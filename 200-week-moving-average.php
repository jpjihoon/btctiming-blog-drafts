  <p class="ko">비트코인 역사상 지난 세 번의 약세장 모두, 가격이 <strong>200주 이동평균선(200-Week Moving Average, 200WMA)</strong> 근처 또는 그 아래에서 최종 저점을 형성했습니다. 단순하지만 놀라울 정도로 신뢰도 높은 이 지표는 "마지노선" 역할을 해왔습니다.</p>
  <p class="en">In all three of Bitcoin's past bear markets, price bottomed near or just below the <strong>200-week moving average (200WMA)</strong>. Simple as it sounds, this indicator has served as a remarkably reliable "line in the sand."</p>
  <p class="ja">ビットコインの過去3回の弱気相場すべてで、価格は<strong>200週移動平均線(200-Week Moving Average, 200WMA)</strong>付近またはその下で最終的な底値を形成しました。シンプルながら驚くほど信頼性の高いこの指標は、「最後の防衛線」として機能してきました。</p>

  <p class="es">En los tres mercados bajistas anteriores de Bitcoin, el precio tocó fondo cerca o justo por debajo de la <strong>media móvil de 200 semanas (200WMA)</strong>. Aunque simple, este indicador ha servido como una "línea de defensa" notablemente confiable.</p>
  <p class="de">In allen drei vergangenen Bitcoin-Bärenmärkten fand der Preis sein Tief nahe oder knapp unter dem <strong>200-Wochen-gleitenden Durchschnitt (200WMA)</strong>. So einfach es klingt, dieser Indikator diente als bemerkenswert zuverlässige "letzte Verteidigungslinie".</p>

