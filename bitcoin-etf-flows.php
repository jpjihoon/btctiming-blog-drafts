  <p class="ko">ETF를 통한 매수는 실물 비트코인을 커스터디에 예치하는 방식이 대부분이라, 선물 기반 상품과 달리 실제 공급량 감소로 이어집니다. 대규모 순유입이 지속되면 유통 공급 자체가 줄어드는 구조적 매수 압력이 만들어집니다.</p>
  <p class="en">Since most ETF-based buying involves depositing actual Bitcoin into custody — unlike futures-based products — it directly reduces circulating supply. Sustained large net inflows create structural buy pressure by shrinking the tradable float.</p>
  <p class="ja">ETFを通じた買いのほとんどは実物ビットコインをカストディに預ける方式のため、先物ベースの商品とは異なり実際の供給量減少につながります。大規模な純流入が続くと、流通供給自体が減少する構造的な買い圧力が生まれます。</p>
  <p class="es">Dado que la mayoría de las compras basadas en ETF implican depositar Bitcoin real en custodia — a diferencia de los productos basados en futuros — reduce directamente el suministro circulante. Entradas netas grandes y sostenidas crean presión de compra estructural al reducir el float negociable.</p>
  <p class="de">Da die meisten ETF-basierten Käufe die Hinterlegung von echtem Bitcoin bei einer Verwahrstelle beinhalten — im Gegensatz zu Futures-basierten Produkten — reduziert dies direkt das zirkulierende Angebot. Anhaltende große Nettozuflüsse erzeugen strukturellen Kaufdruck, indem sie den handelbaren Float verringern.</p>

