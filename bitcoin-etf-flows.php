  <ul class="en">
    <li><strong>Highly volatile day to day.</strong> A single large-flow day can distort the apparent trend — smoothing with a 5–10 day moving average is recommended.</li>
    <li><strong>May include arbitrage flows.</strong> Some inflows are basis-trade (spot-futures arbitrage) activity, not purely directional bets — worth distinguishing.</li>
  </ul>
