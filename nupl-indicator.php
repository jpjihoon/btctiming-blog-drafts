  <ul class="ko">
    <li><strong>MVRV 계열 지표와 상관관계가 높음.</strong> 같은 실현시가총액 데이터를 쓰기 때문에 MVRV Z-Score와 유사하게 움직입니다. 두 지표를 모두 매수 근거로 쓰면 중복 확인일 뿐, 독립적인 신호는 아닙니다.</li>
    <li><strong>사이클마다 극단값이 달라질 수 있음.</strong> 시장이 성숙해지며 도취 단계의 최고치가 사이클마다 낮아지는 경향이 있습니다.</li>
    <li><strong>타이밍 지표가 아님.</strong> 항복 구간에 진입했다고 해서 즉시 반등하는 것은 아니며, 수개월간 항복 상태가 지속될 수 있습니다.</li>
  </ul>
  <ul class="en">
    <li><strong>Highly correlated with MVRV-family metrics.</strong> Since it uses the same realized cap data, it moves similarly to MVRV Z-Score. Using both as buy triggers is redundant confirmation, not two independent signals.</li>
    <li><strong>Extremes can shift across cycles.</strong> As the market matures, euphoria-zone peaks have tended to get lower each cycle.</li>
    <li><strong>Not a precise timing tool.</strong> Entering the capitulation zone doesn't mean an immediate rebound — it can persist for months.</li>
  </ul>
  <ul class="ja">
    <li><strong>MVRV系指標との相関が高い。</strong> 同じ実現時価総額データを使うため、MVRV Zスコアと似た動きをします。両方を買いの根拠にするのは重複確認に過ぎず、独立したシグナルではありません。</li>
    <li><strong>サイクルごとに極値が変わる可能性がある。</strong> 市場が成熟するにつれ、陶酔段階のピークがサイクルごとに低くなる傾向があります。</li>
    <li><strong>タイミングツールではない。</strong> キャピチュレーション圏に入ったからといって即座に反発するわけではなく、数ヶ月間その状態が続くことがあります。</li>
  </ul>
  <ul class="es">
    <li><strong>Alta correlación con métricas de la familia MVRV.</strong> Como usa los mismos datos de capitalización realizada, se mueve de manera similar al MVRV Z-Score. Usar ambos como disparadores de compra es confirmación redundante, no dos señales independientes.</li>
    <li><strong>Los extremos pueden cambiar entre ciclos.</strong> A medida que el mercado madura, los picos de la zona de euforia han tendido a ser más bajos en cada ciclo.</li>
    <li><strong>No es una herramienta de timing precisa.</strong> Entrar en la zona de capitulación no significa un rebote inmediato — puede persistir durante meses.</li>
  </ul>
  <ul class="de">
    <li><strong>Hohe Korrelation mit MVRV-Familienmetriken.</strong> Da dieselben Realized-Cap-Daten verwendet werden, bewegt es sich ähnlich wie der MVRV Z-Score. Beide als Kaufauslöser zu verwenden ist redundante Bestätigung, keine zwei unabhängigen Signale.</li>
    <li><strong>Extremwerte können sich zyklusübergreifend verschieben.</strong> Mit zunehmender Marktreife sind die Höchststände der Euphoriezone tendenziell in jedem Zyklus niedriger geworden.</li>
    <li><strong>Kein präzises Timing-Tool.</strong> Der Eintritt in die Kapitulationszone bedeutet keine sofortige Erholung — sie kann monatelang anhalten.</li>
