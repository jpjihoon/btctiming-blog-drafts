  <h2 class="ko">패턴이 보인다 — 데이터에서 실행으로</h2>
  <h2 class="en">A pattern emerges — from data to execution</h2>
  <h2 class="ja">パターンが見える — データから実行へ</h2>
  <h2 class="es">Emerge un Patrón — De Datos a Ejecución</h2>
  <h2 class="de">Ein Muster entsteht — Von Daten zu Ausführung</h2>
  <p class="ko">앞의 다섯 업그레이드를 보면 순서가 있어요. 먼저 합의 메커니즘을 바꾸고(머지), 스테이킹을 완성하고(샤펠라), 데이터 처리 용량을 늘리고(덴쿤, 푸사카)... 즉 "L2가 더 많은 데이터를 더 싸게 처리할 수 있게" 만드는 데 집중해왔습니다. 그런데 이 데이터를 실제로 빠르게 실행하는 능력(L1 자체의 처리 속도)은 상대적으로 손대지 않고 있었어요.</p>
  <p class="en">Look at the first five upgrades and there's a sequence: change the consensus mechanism (Merge), complete staking (Shapella), expand data capacity (Dencun, Fusaka). The focus has been on letting L2s process more data more cheaply. The ability to actually execute that data quickly — L1's own processing speed — had been left largely untouched.</p>
  <p class="ja">先の5つのアップグレードを見ると順序があります。まずコンセンサスメカニズムを変え(Merge)、ステーキングを完成させ(Shapella)、データ処理容量を増やす(Dencun、Fusaka)…つまり「L2がより多くのデータをより安く処理できるように」することに集中してきました。しかしこのデータを実際に速く実行する能力(L1自体の処理速度)は相対的に手つかずのままでした。</p>

  <p class="es">Miren las primeras cinco actualizaciones y hay una secuencia: cambiar el mecanismo de consenso (Merge), completar el staking (Shapella), expandir la capacidad de datos (Dencun, Fusaka). El enfoque ha sido permitir que las L2 procesen más datos de forma más barata. La capacidad de ejecutar realmente esos datos rápidamente — la propia velocidad de procesamiento de L1 — se había dejado en gran medida intacta.</p>
  <p class="de">Schaut man sich die ersten fünf Upgrades an, gibt es eine Sequenz: den Konsensmechanismus ändern (Merge), Staking vervollständigen (Shapella), Datenkapazität erweitern (Dencun, Fusaka). Der Fokus lag darauf, L2s mehr Daten günstiger verarbeiten zu lassen. Die Fähigkeit, diese Daten tatsächlich schnell auszuführen — L1s eigene Verarbeitungsgeschwindigkeit — war weitgehend unangetastet geblieben.</p>

  <p class="ko">Glamsterdam이 이 부분을 정면으로 건드립니다. 지금 이더리움 블록의 80~90%는 MEV-Boost 같은 외부 중계자를 통해 만들어지는데, ePBS(EIP-7732)는 이 과정을 프로토콜 안으로 가져와서 소수 대형 빌더에 대한 의존을 줄여요. 여기에 블록레벨 접근목록(EIP-7928)이 어떤 트랜잭션이 서로 겹치지 않는지 미리 알려주면서, 순차 처리 대신 병렬 처리로 가는 길을 열어줍니다.</p>
  <p class="en">Glamsterdam tackles this head-on. Currently, 80-90% of Ethereum blocks are built through external relays like MEV-Boost. ePBS (EIP-7732) pulls that process into the protocol, reducing reliance on a handful of dominant builders. Combined with block-level access lists (EIP-7928), which pre-declare which transactions don't overlap, this opens the door from sequential to parallel execution.</p>
  <p class="ja">Glamsterdamはこの部分を正面から扱います。現在イーサリアムブロックの80〜90%はMEV-Boostのような外部リレーを通じて作られていますが、ePBS(EIP-7732)はこのプロセスをプロトコル内に取り込み、少数の大手ビルダーへの依存を減らします。ここにブロックレベルアクセスリスト(EIP-7928)がどのトランザクションが互いに重複しないかを事前に知らせることで、逐次処理から並列処理への道を開きます。</p>
  <p class="es">Glamsterdam aborda esto de frente. Actualmente, el 80-90% de los bloques de Ethereum se construyen a través de relés externos como MEV-Boost. ePBS (EIP-7732) lleva ese proceso al protocolo, reduciendo la dependencia de un puñado de constructores dominantes. Combinado con listas de acceso a nivel de bloque (EIP-7928), que declaran de antemano qué transacciones no se superponen, esto abre la puerta de la ejecución secuencial a la paralela.</p>
  <p class="de">Glamsterdam geht dies direkt an. Derzeit werden 80-90% der Ethereum-Blöcke über externe Relays wie MEV-Boost gebaut. ePBS (EIP-7732) zieht diesen Prozess in das Protokoll und reduziert die Abhängigkeit von einer Handvoll dominanter Builder. Kombiniert mit Block-Level-Access-Listen (EIP-7928), die vorab deklarieren, welche Transaktionen sich nicht überschneiden, öffnet dies die Tür von sequenzieller zu paralleler Ausführung.</p>

  <h2 class="ko">다음은 Hegotá</h2>
  <h2 class="en">Next up: Hegotá</h2>
  <h2 class="ja">次はHegotá</h2>
  <h2 class="es">A Continuación: Hegotá</h2>
  <h2 class="de">Als Nächstes: Hegotá</h2>
  <p class="ko">Glamsterdam 이후엔 Hegotá가 예정돼 있어요. 핵심 후보는 Verkle Trees — 노드가 전체 상태를 저장하지 않고도 블록을 검증할 수 있게 해주는 데이터 구조로, 이게 들어오면 개인이 집에서 풀노드를 돌리는 데 필요한 하드웨어 요구사항이 크게 낮아집니다. 지금의 흐름(데이터 확장 → 실행 병렬화)에 이어, 그다음은 "누구나 검증자가 될 수 있는 문턱을 낮추는" 방향으로 가는 셈이에요.</p>
  <p class="en">After Glamsterdam comes Hegotá. The leading candidate is Verkle Trees — a data structure letting nodes verify blocks without storing the full state, which would significantly lower the hardware bar for running a home full node. Following the current arc (expand data → parallelize execution), the next step is lowering the barrier for anyone to become a validator.</p>
  <p class="ja">Glamsterdamの後にはHegotáが予定されています。有力候補はVerkle Trees — ノードが全体の状態を保存しなくてもブロックを検証できるデータ構造で、これが導入されれば個人が自宅でフルノードを運用するのに必要なハードウェア要件が大幅に下がります。現在の流れ(データ拡張→実行の並列化)に続き、次は「誰もがバリデーターになれる敷居を下げる」方向へ進むわけです。</p>

  <p class="es">Después de Glamsterdam viene Hegotá. El principal candidato es Verkle Trees — una estructura de datos que permite a los nodos verificar bloques sin almacenar el estado completo, lo que reduciría significativamente el requisito de hardware para ejecutar un nodo completo en casa. Siguiendo el arco actual (expandir datos → paralelizar ejecución), el siguiente paso es reducir la barrera para que cualquiera pueda convertirse en validador.</p>
