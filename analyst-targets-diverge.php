  <p class="ko">시티는 <a href="/blog/etf-outflow-worst-month.html">ETF 자금 유출</a>, 부진한 투자자 관심, <a href="/blog/clarity-act-stalled.html">미국 크립토 입법 지연</a>을 근거로 목표가를 낮췄어요. 반면 스탠다드차타드의 제프리 켄드릭은 지금의 약세가 오히려 ETF 압력이 해소되면 매수 구간이 될 수 있다고 봤고, 번스타인은 연초부터 "이미 바닥을 찍었다"는 입장을 유지하고 있습니다. 셋 다 같은 사실을 알고 있지만, <strong>ETF 매도 압력이 언제 끝날 것인가</strong>에 대한 가정이 완전히 달랐던 거예요.</p>
  <p class="en">Citi cited <a href="/blog/etf-outflow-worst-month.html">ETF outflows</a>, weak investor interest, and <a href="/blog/clarity-act-stalled.html">stalled US crypto legislation</a> for its downgrade. Standard Chartered's Geoffrey Kendrick instead views current weakness as a potential buy zone once ETF pressure fades. Bernstein has maintained since early in the year that a bottom is already in. All three know the same facts — they simply hold completely different assumptions about <strong>when ETF sell pressure ends</strong>.</p>
  <p class="ja">シティは<a href="/blog/etf-outflow-worst-month.html">ETF資金流出</a>、投資家の関心低下、<a href="/blog/clarity-act-stalled.html">米国暗号資産立法の停滞</a>を根拠に目標価格を下げました。一方スタンダードチャータードのジェフリー・ケンドリックは、今の弱さがむしろETF圧力が解消されれば買いゾーンになり得ると見ており、バーンスタインは年初から「すでに底を打った」との立場を維持しています。3社とも同じ事実を知っていますが、<strong>ETF売り圧力がいつ終わるか</strong>についての前提がまったく異なっていたのです。</p>

  <p class="es">Citi citó <a href="/blog/etf-outflow-worst-month.html">salidas de ETF</a>, débil interés de los inversores, y <a href="/blog/clarity-act-stalled.html">legislación cripto estadounidense estancada</a> para su rebaja. Geoffrey Kendrick de Standard Chartered en cambio ve la debilidad actual como una posible zona de compra una vez que la presión de ETF se desvanezca. Bernstein ha mantenido desde principios de año que ya se tocó fondo. Los tres conocen los mismos hechos — simplemente tienen supuestos completamente diferentes sobre <strong>cuándo termina la presión de venta de ETF</strong>.</p>

