  <p class="ko">목표가 자체보다, <strong>왜 갈렸는지</strong>를 이해하는 게 훨씬 유용해요. 세 기관 모두 "ETF 자금 흐름이 방향을 결정한다"는 데는 동의하고 있거든요. 즉 애널리스트 목표가를 따라가기보다, 이 공통 전제(ETF 흐름 반전 여부)를 직접 추적하는 게 더 실용적인 접근입니다.</p>
  <p class="en">Understanding <strong>why</strong> the split exists matters more than the targets themselves. All three institutions agree that ETF flows will determine direction. Rather than following any single target, tracking that shared premise — whether ETF flows actually reverse — directly is the more practical approach.</p>
  <p class="ja">目標価格そのものより、<strong>なぜ分かれたのか</strong>を理解する方がはるかに有用です。3機関とも「ETF資金フローが方向性を決める」ことには同意しています。つまりアナリストの目標価格を追うより、この共通の前提(ETFフローが実際に反転するか)を直接追跡する方が実用的なアプローチです。</p>
  <p class="es">Entender <strong>por qué</strong> existe la división importa más que los objetivos en sí. Las tres instituciones coinciden en que los flujos de ETF determinarán la dirección. En lugar de seguir un solo objetivo, rastrear directamente esa premisa compartida — si los flujos de ETF realmente se revierten — es el enfoque más práctico.</p>
  <p class="de">Zu verstehen, <strong>warum</strong> die Kluft besteht, ist wichtiger als die Ziele selbst. Alle drei Institutionen stimmen darin überein, dass ETF-Flüsse die Richtung bestimmen werden. Statt einem einzelnen Ziel zu folgen, ist es der praktischere Ansatz, diese gemeinsame Prämisse direkt zu verfolgen — ob sich die ETF-Flüsse tatsächlich umkehren.</p>
