  <h2 class="en">How Hash Ribbon Works — Reading Miner Psychology</h2>
  <h2 class="ja">Hash Ribbonの原理 — マイナー心理を読む</h2>
  <p class="ko">비트코인 채굴자들은 가격 하락기에 가장 취약합니다. 전기료, 장비 감가 등 고정 비용 때문에 가격이 일정 수준 이하로 내려가면 채굴을 중단하고 보유 BTC를 팔기 시작합니다. 이를 <strong>채굴자 항복(Miner Capitulation)</strong>이라고 합니다.</p>
  <p class="en">Bitcoin miners are the most vulnerable during price downturns. With fixed costs — electricity, equipment depreciation — when prices fall below break-even, miners shut down and start selling their BTC. This is called <strong>Miner Capitulation</strong>.</p>
  <p class="ja">ビットコインのマイナーは、価格下落局面で最も脆弱です。電気代や機材の減価償却といった固定費のため、価格が一定水準を下回ると採掘を停止し、保有BTCを売り始めます。これを<strong>マイナーキャピチュレーション</strong>と呼びます。</p>

  <p class="es">Los mineros de Bitcoin son los más vulnerables durante las caídas de precio. Con costos fijos — electricidad, depreciación de equipos — cuando los precios caen por debajo del punto de equilibrio, los mineros cierran y comienzan a vender su BTC. Esto se llama <strong>Capitulación de Mineros</strong>.</p>
  <p class="de">Bitcoin-Miner sind während Preiseinbrüchen am anfälligsten. Bei fixen Kosten — Strom, Geräteabschreibung — schalten Miner ab und beginnen, ihr BTC zu verkaufen, wenn die Preise unter den Break-even fallen. Dies wird <strong>Miner-Kapitulation</strong> genannt.</p>

