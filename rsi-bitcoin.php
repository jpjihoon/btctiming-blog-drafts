  <ul class="en">
    <li><strong>Overbought conditions can persist in bull markets.</strong> Treating RSI above 70 as an immediate sell signal can mean missing large gains.</li>
    <li><strong>Signals differ by timeframe.</strong> Daily and weekly RSI can point in different directions and should be checked together.</li>
  </ul>
  <ul class="ja">
